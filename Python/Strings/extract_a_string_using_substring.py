my_string = "Hello, My name is Sravani"

substring1 = my_string[7:]  

substring2 = my_string[0:5]  

substring3 = my_string[10:25]  

substring4 = my_string[::2]  

print("Substring from index 7 to end:", substring1)
print("Substring from index 0 to 5:", substring2)
print("Substring from index 10 to 25:", substring3)
print("Substring with step value 2:", substring4)
