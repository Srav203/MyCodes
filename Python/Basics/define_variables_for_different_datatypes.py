# Integer
int_var = 55
print("Integer:", int_var)

# Float (equivalent to double in Python)
float_var = 1.16129
print("Float:", float_var)

# Boolean
bool_var = True
print("Boolean:", bool_var)

# Character (Python uses strings for characters)
str = 'laddo'
print("Character:", str)

# Double (Python uses float for double precision)
double_var = 3.1415921253529793
print("Double:", double_var)
